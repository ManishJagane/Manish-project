package Loop_Study;



public class for_loop {

	public static void main(String[] args) 
	{
		//loop are use for Repetitive value
		//ex. if i want to print 0 to 10 and increment by 1
		
		for (int a =0; a<=10; a++)
		{
			System.out.println("Roll no. "+a);
			
		}
		
		//If i want to print 2s table is increment  by 2
		
		for (int a=2; a<=20; a=a+2)
		{
			System.out.println(a);
		}
		// if we want decrement 2s table then 
		
		for(int a=20; a>=0; a= a-2)
		{
			System.out.println(a);
		}
		for(int a=1; a<=5; a++)
		{
			System.out.println("MANISH");
		}

	}

}
